public class Student {

    private String name;
    private String clazz;
    private int age ;


    public String getName() {
        return name;
    }

    public String getClazz() {
        return clazz;
    }

    public int getAge() {
        return age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setClazz(String clazz) {
        this.clazz = clazz;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
